package com.codingjx.fundamental.number;

public class NumberSystemDemo01 {
    public static void main(String[] args) {
        // 十進制
        int num1 = 103;
        System.out.println("num1: " + num1);

        // 二進制
        int num2 = 0b10;
        System.out.println("num2: " + num2);

        // 十六進制
        int num3 = 0X23a;
        System.out.println("num3: " + num3);
    }
}
